class PaginaDiccionario4Life extends StatefulWidget {
  const PaginaDiccionario4Life({super.key});

  @override
  State<PaginaDiccionario4Life> createState() => _PaginaDiccionario4LifeState();
}

class _PaginaDiccionario4LifeState extends State<PaginaDiccionario4Life> {
  final TextEditingController _busquedaController = TextEditingController();
  String _busqueda = '';
  String _categoria = 'todos';

  @override
  void dispose() {
    _busquedaController.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    final ingles = IdiomaService.actual.value == IdiomaApp.ingles;
    final entradas = entradasDiccionario4Life.where((entrada) {
      final texto = _normalizar(
        '${entrada.termino} ${entrada.conceptoEs} ${entrada.conceptoEn}',
      );
      final coincideBusqueda =
          _busqueda.trim().isEmpty || texto.contains(_normalizar(_busqueda));
      final coincideCategoria = _categoria == 'todos' ||
          _categoriaEntrada(entrada).contains(_categoria);
      return coincideBusqueda && coincideCategoria;
    }).toList();

    return Scaffold(
      backgroundColor: Colors.white,
      body: Column(
        children: [
          Container(
            padding: const EdgeInsets.fromLTRB(22, 18, 22, 28),
            decoration: const BoxDecoration(
              gradient: LinearGradient(
                begin: Alignment.topLeft,
                end: Alignment.bottomRight,
                colors: [Color(0xFF172B98), Color(0xFF06105A)],
              ),
            ),
            child: SafeArea(
              bottom: false,
              child: Column(
                children: [
                  _encabezado(context, ingles),
                  const SizedBox(height: 28),
                  _buscador(ingles),
                ],
              ),
            ),
          ),
          Expanded(
            child: Transform.translate(
              offset: const Offset(0, -18),
              child: ListView(
                padding: const EdgeInsets.fromLTRB(16, 0, 16, 24),
                children: [
                  Container(
                    padding: const EdgeInsets.fromLTRB(18, 24, 18, 24),
                    decoration: BoxDecoration(
                      color: Colors.white,
                      borderRadius: BorderRadius.circular(18),
                      border: Border.all(color: const Color(0xFFE5E8F5)),
                      boxShadow: [
                        BoxShadow(
                          color:
                              const Color(0xFF0B176B).withValues(alpha: 0.08),
                          blurRadius: 28,
                          offset: const Offset(0, 10),
                        ),
                      ],
                    ),
                    child: Column(
                      crossAxisAlignment: CrossAxisAlignment.start,
                      children: [
                        _tituloCategorias(ingles),
                        const SizedBox(height: 18),
                        _filtrosCategorias(ingles),
                        const SizedBox(height: 20),
                        const Divider(color: Color(0xFFE2E5F0), height: 1),
                        const SizedBox(height: 20),
                        if (entradas.isEmpty)
                          _estadoVacio(ingles)
                        else
                          Wrap(
                            spacing: 12,
                            runSpacing: 12,
                            children: [
                              for (final entrada in entradas)
                                _chipEntrada(context, entrada, ingles),
                            ],
                          ),
                        const SizedBox(height: 26),
                        _ayudaBusqueda(ingles),
                      ],
                    ),
                  ),
                ],
              ),
            ),
          ),
        ],
      ),
    );
  }

  Widget _encabezado(BuildContext context, bool ingles) {
    return Row(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        IconButton(
          onPressed: () => Navigator.pop(context),
          icon: const Icon(Icons.arrow_back_rounded, color: Colors.white),
          iconSize: 34,
          padding: EdgeInsets.zero,
          constraints: const BoxConstraints.tightFor(width: 44, height: 44),
        ),
        const SizedBox(width: 12),
        Expanded(
          child: Column(
            crossAxisAlignment: CrossAxisAlignment.start,
            children: [
              Text(
                ingles ? 'Dictionary' : 'Diccionario',
                style: const TextStyle(
                  color: Colors.white,
                  fontSize: 31,
                  height: 1.1,
                  fontWeight: FontWeight.w700,
                ),
              ),
              const SizedBox(height: 9),
              Text(
                ingles
                    ? 'Key terms for products and wellness'
                    : 'Términos clave de productos y bienestar',
                style: const TextStyle(
                  color: Color(0xFFD9DFFF),
                  fontSize: 18,
                  height: 1.2,
                  fontWeight: FontWeight.w500,
                ),
              ),
            ],
          ),
        ),
      ],
    );
  }

  Widget _buscador(bool ingles) {
    return Container(
      height: 66,
      decoration: BoxDecoration(
        color: Colors.white,
        borderRadius: BorderRadius.circular(34),
        boxShadow: [
          BoxShadow(
            color: Colors.black.withValues(alpha: 0.08),
            blurRadius: 16,
            offset: const Offset(0, 8),
          ),
        ],
      ),
      child: TextField(
        controller: _busquedaController,
        onChanged: (valor) => setState(() => _busqueda = valor),
        decoration: InputDecoration(
          hintText: ingles ? 'Search term...' : 'Buscar termino...',
          hintStyle: const TextStyle(
            color: Color(0xFF7A819F),
            fontSize: 18,
            fontWeight: FontWeight.w500,
          ),
          prefixIcon: const Icon(
            Icons.search_rounded,
            color: Color(0xFF172394),
            size: 32,
          ),
          suffixIcon: _busqueda.isEmpty
              ? null
              : IconButton(
                  onPressed: () {
                    _busquedaController.clear();
                    setState(() => _busqueda = '');
                  },
                  icon: const Icon(Icons.close_rounded),
                  color: const Color(0xFF66708F),
                ),
          border: InputBorder.none,
          contentPadding:
              const EdgeInsets.symmetric(horizontal: 10, vertical: 20),
        ),
      ),
    );
  }

  Widget _tituloCategorias(bool ingles) {
    return Row(
      children: [
        Container(
          width: 54,
          height: 54,
          decoration: const BoxDecoration(
            color: Color(0xFFEFF1FF),
            shape: BoxShape.circle,
          ),
          child: const Icon(
            Icons.menu_book_rounded,
            color: Color(0xFF2839C7),
            size: 30,
          ),
        ),
        const SizedBox(width: 16),
        Expanded(
          child: Column(
            crossAxisAlignment: CrossAxisAlignment.start,
            children: [
              Text(
                ingles ? 'Explore by category' : 'Explora por categorias',
                style: const TextStyle(
                  color: Color(0xFF07136E),
                  fontSize: 18,
                  fontWeight: FontWeight.w900,
                ),
              ),
              const SizedBox(height: 5),
              Text(
                ingles
                    ? 'Find terms related to 4Life and wellness'
                    : 'Encuentra terminos relacionados con 4Life y tu bienestar',
                style: const TextStyle(
                  color: Color(0xFF2F3A78),
                  fontSize: 14,
                  height: 1.25,
                  fontWeight: FontWeight.w500,
                ),
              ),
            ],
          ),
        ),
      ],
    );
  }

  Widget _filtrosCategorias(bool ingles) {
    return SingleChildScrollView(
      scrollDirection: Axis.horizontal,
      child: Row(
        children: [
          for (final categoria in _categoriasDiccionario) ...[
            _chipCategoria(categoria, ingles),
            const SizedBox(width: 10),
          ],
        ],
      ),
    );
  }

  Widget _chipCategoria(_CategoriaDiccionario categoria, bool ingles) {
    final seleccionado = categoria.clave == _categoria;
    return InkWell(
      borderRadius: BorderRadius.circular(16),
      onTap: () => setState(() => _categoria = categoria.clave),
      child: AnimatedContainer(
        duration: const Duration(milliseconds: 180),
        padding: const EdgeInsets.symmetric(horizontal: 16, vertical: 14),
        decoration: BoxDecoration(
          gradient: seleccionado
              ? const LinearGradient(
                  colors: [Color(0xFF5145D8), Color(0xFF2839C7)],
                )
              : null,
          color: seleccionado ? null : const Color(0xFFF8F8FE),
          borderRadius: BorderRadius.circular(16),
          border: Border.all(
            color: seleccionado
                ? const Color(0xFF5145D8)
                : const Color(0xFFE5E7F3),
          ),
        ),
        child: Row(
          mainAxisSize: MainAxisSize.min,
          children: [
            Icon(
              categoria.icono,
              color: seleccionado ? Colors.white : const Color(0xFF2839C7),
              size: 20,
            ),
            const SizedBox(width: 9),
            Text(
              ingles ? categoria.etiquetaEn : categoria.etiquetaEs,
              style: TextStyle(
                color: seleccionado ? Colors.white : const Color(0xFF17204B),
                fontSize: 14,
                fontWeight: FontWeight.w900,
              ),
            ),
          ],
        ),
      ),
    );
  }

  Widget _chipEntrada(
    BuildContext context,
    EntradaDiccionario4Life entrada,
    bool ingles,
  ) {
    return InkWell(
      borderRadius: BorderRadius.circular(16),
      onTap: () => _mostrarConcepto(context, entrada, ingles),
      child: Container(
        padding: const EdgeInsets.symmetric(horizontal: 16, vertical: 13),
        decoration: BoxDecoration(
          color: const Color(0xFFF8F8FE),
          borderRadius: BorderRadius.circular(16),
          border: Border.all(color: const Color(0xFFE6E8F4)),
          boxShadow: [
            BoxShadow(
              color: const Color(0xFF172394).withValues(alpha: 0.03),
              blurRadius: 10,
              offset: const Offset(0, 5),
            ),
          ],
        ),
        child: Row(
          mainAxisSize: MainAxisSize.min,
          children: [
            Icon(
              _iconoEntrada(entrada),
              color: const Color(0xFF2839C7),
              size: 22,
            ),
            const SizedBox(width: 11),
            Text(
              entrada.termino,
              style: const TextStyle(
                color: Color(0xFF17204B),
                fontSize: 14.5,
                fontWeight: FontWeight.w900,
              ),
            ),
          ],
        ),
      ),
    );
  }

  Widget _estadoVacio(bool ingles) {
    return Container(
      width: double.infinity,
      padding: const EdgeInsets.symmetric(vertical: 34, horizontal: 16),
      alignment: Alignment.center,
      child: Text(
        ingles
            ? 'No matching terms found.'
            : 'No se encontraron terminos relacionados.',
        textAlign: TextAlign.center,
        style: const TextStyle(
          color: Color(0xFF56618F),
          fontSize: 16,
          fontWeight: FontWeight.w700,
        ),
      ),
    );
  }

  Widget _ayudaBusqueda(bool ingles) {
    return Container(
      width: double.infinity,
      padding: const EdgeInsets.all(18),
      decoration: BoxDecoration(
        color: const Color(0xFFF2F4FF),
        borderRadius: BorderRadius.circular(18),
      ),
      child: Row(
        children: [
          Container(
            width: 58,
            height: 58,
            decoration: const BoxDecoration(
              color: Color(0xFFE2E7FF),
              shape: BoxShape.circle,
            ),
            child: const Icon(
              Icons.lightbulb_outline_rounded,
              color: Color(0xFF2839C7),
              size: 32,
            ),
          ),
          const SizedBox(width: 16),
          Expanded(
            child: Column(
              crossAxisAlignment: CrossAxisAlignment.start,
              children: [
                Text(
                  ingles
                      ? 'Cannot find what you need?'
                      : 'No encuentras lo que buscas?',
                  style: const TextStyle(
                    color: Color(0xFF07136E),
                    fontSize: 16,
                    fontWeight: FontWeight.w900,
                  ),
                ),
                const SizedBox(height: 4),
                Text(
                  ingles
                      ? 'Use the search bar to find specific terms.'
                      : 'Usa el buscador para encontrar terminos especificos.',
                  style: const TextStyle(
                    color: Color(0xFF2F3A78),
                    fontSize: 14,
                    height: 1.25,
                    fontWeight: FontWeight.w500,
                  ),
                ),
              ],
            ),
          ),
        ],
      ),
    );
  }

