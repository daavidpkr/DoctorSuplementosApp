  List<String> _categoriaEntrada(EntradaDiccionario4Life entrada) {
    final texto = _normalizar('${entrada.termino} ${entrada.conceptoEs}');
    final categorias = <String>{};
    if (texto.contains('transfer') ||
        texto.contains('inmun') ||
        texto.contains('defensa') ||
        texto.contains('nk') ||
        texto.contains('calostro') ||
        texto.contains('ovofactor') ||
        texto.contains('nanofactor')) {
      categorias.add('inmunidad');
    }
    if (texto.contains('vitamina') ||
        texto.contains('zinc') ||
        texto.contains('fruta') ||
        texto.contains('nutric') ||
        texto.contains('prebi') ||
        texto.contains('colageno') ||
        texto.contains('alimento') ||
        texto.contains('hongo')) {
      categorias.add('nutricion');
    }
    if (texto.contains('riovida') ||
        texto.contains('plus') ||
        texto.contains('stix') ||
        texto.contains('producto') ||
        texto.contains('formula') ||
        texto.contains('targeted')) {
      categorias.add('productos');
    }
    if (texto.contains('bienestar') ||
        texto.contains('salud') ||
        texto.contains('digest') ||
        texto.contains('vitalidad') ||
        texto.contains('celular') ||
        texto.contains('dosis')) {
      categorias.add('bienestar');
    }
    return categorias.isEmpty ? const ['bienestar'] : categorias.toList();
  }

  IconData _iconoEntrada(EntradaDiccionario4Life entrada) {
    final categorias = _categoriaEntrada(entrada);
    if (categorias.contains('productos')) return Icons.medication_outlined;
    if (categorias.contains('inmunidad')) return Icons.shield_outlined;
    if (categorias.contains('nutricion')) return Icons.eco_outlined;
    return Icons.favorite_border_rounded;
  }

  String _normalizar(String texto) {
    return texto
        .toLowerCase()
        .replaceAll(RegExp(r'[áàäâ]'), 'a')
        .replaceAll(RegExp(r'[éèëê]'), 'e')
        .replaceAll(RegExp(r'[íìïî]'), 'i')
        .replaceAll(RegExp(r'[óòöô]'), 'o')
        .replaceAll(RegExp(r'[úùüû]'), 'u')
        .replaceAll('ñ', 'n')
        .replaceAll(RegExp(r'[^a-z0-9 ]'), ' ')
        .replaceAll(RegExp(r'\s+'), ' ')
        .trim();
  }
